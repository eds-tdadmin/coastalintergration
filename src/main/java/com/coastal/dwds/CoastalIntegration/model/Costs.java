package com.coastal.dwds.CoastalIntegration.model;

public class Costs {
	
	private Double dailycost;
	private Double cummcost;
	private Double afecost;
	private Double suppafe;
	private String expenditure;
	private String afeno;
	
	public Double getDailycost() {
		return dailycost;
	}
	public void setDailycost(Double dailycost) {
		this.dailycost = dailycost;
	}
	public Double getCummcost() {
		return cummcost;
	}
	public void setCummcost(Double cummcost) {
		this.cummcost = cummcost;
	}
	public Double getAfecost() {
		return afecost;
	}
	public void setAfecost(Double afecost) {
		this.afecost = afecost;
	}
	public Double getSuppafe() {
		return suppafe;
	}
	public void setSuppafe(Double suppafe) {
		this.suppafe = suppafe;
	}
	public String getExpenditure() {
		return expenditure;
	}
	public void setExpenditure(String expenditure) {
		this.expenditure = expenditure;
	}
	public String getAfeno() {
		return afeno;
	}
	public void setAfeno(String afeno) {
		this.afeno = afeno;
	}

}
